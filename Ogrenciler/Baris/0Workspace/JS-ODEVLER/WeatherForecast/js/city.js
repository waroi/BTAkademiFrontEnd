class City {
  constructor(
    todayDate,
    name,
    country,
    todayDegree,
    todayIcon,
    todayInfo,
    todayFeels,
    todayWind,
    todayHumidity
  ) {
    this.todayDate = todayDate;
    this.name = name;
    this.country = country;
    this.todayDegree = todayDegree;
    this.todayIcon = todayIcon;
    this.todayInfo = todayInfo;
    this.todayFeels = todayFeels;
    this.todayWind = todayWind;
    this.todayHumidity = todayHumidity;
  }
}
